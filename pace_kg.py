MERGE_SIM_THRESHOLD   = 0.92     # merge two concepts if cosine >= this
REVIEW_SIM_THRESHOLD  = 0.75     # flag for human review if between 0.75-0.92

# Weight signal proportions
W_EVIDENCE = 0.50
W_SLIDE    = 0.30
W_DOC      = 0.20

# ── Relation role boosts ──────────────────────────────────────────────────────
# (relation, role) → boost
# role: 'subject' or 'object'
RELATION_ROLE_BOOSTS = {
    ("isDefinedAs",        "object"):  +0.15,
    ("isPrerequisiteOf",   "subject"): +0.10,
    ("isGeneralizationOf", "object"):  +0.10,
    ("contrastedWith",     "subject"): +0.05,
    ("contrastedWith",     "object"):  +0.05,
    ("causeOf",            "subject"): +0.05,
    ("isExampleOf",        "subject"): -0.05,
}

# ── Source type boosts ────────────────────────────────────────────────────────
SOURCE_TYPE_BOOSTS = {
    "heading": +0.10,
    "bullet":  +0.05,
    "table":   +0.05,
    "body":    +0.00,
    "caption": -0.05,
}

# ── Data model ────────────────────────────────────────────────────────────────
@dataclass
class ConceptNode:
    name:            str
    aliases:         List[str]
    slide_ids:       List[str]
    source_type:     str          # heading / body / bullet / table / caption
    keyphrase_score: float        # raw GLiNER score from Step 3
    final_weight:    float
    doc_id:          str
    needs_review:    bool = False  # flagged when similar to another concept

