def resolve_concept_name(name: str) -> str:
    """
    Return the canonical concept name. If the incoming name is very similar
    to an existing concept (>= MERGE threshold), return that concept's name.
    """
    name_lower = name.lower().strip()
    # Direct match first
    if name_lower in concept_by_name:
        return concept_by_name[name_lower]["name"]
