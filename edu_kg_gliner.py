# -- Helpers ------------------------------------------------------------------
def _assign_source_type(phrase: str, sc) -> str:
    pl = phrase.lower()
    for h in sc.headings:
        if pl in h.lower(): return "heading"
    for b in sc.bullets:
        if pl in b.lower(): return "bullet"
    for t in sc.table_cells:
        if pl in t.lower(): return "table"
    for c in sc.captions:
        if pl in c.lower(): return "caption"
    return "body"

def _find_sentence(phrase: str, text: str) -> str:
    if not text:
        return ""
    doc = nlp(text)
    pl  = phrase.lower()
    for sent in doc.sents:
        if pl in sent.text.lower():
            return sent.text.strip()
    return text[:200]

def _deduplicate(kps: List[Keyphrase]) -> List[Keyphrase]:
    if len(kps) <= 1:
        return kps
    sorted_kps = sorted(kps, key=lambda k: k.score, reverse=True)
    kept: List[Keyphrase] = []
    for kp in sorted_kps:
        emb = minilm.encode(kp.phrase, convert_to_tensor=True)
        is_dup = any(
            float(st_util.cos_sim(
                emb,
                minilm.encode(k.phrase, convert_to_tensor=True)
            )) >= DEDUP_SIM_THRESHOLD
            for k in kept
        )
        if not is_dup:
            kept.append(kp)
    return kept

# -- LLM keyphrase fallback ---------------------------------------------------
# Used ONLY when GLiNER returns 0 phrases on a slide with ≥ 20 words.
# Fires on ~4-6 slides per PDF (table-only slides, very sparse slides).
# Uses the same Groq key as Step 4 — no new dependency.
# Assigned score: 0.40 (below GLiNER confident hits, above noise).

_KP_FALLBACK_SYSTEM = """You are extracting educational keyphrases from a single lecture slide.

Return ONLY a JSON object: {"keyphrases": ["phrase1", "phrase2", ...]}

Rules:
- Maximum 12 phrases.
- Only include named concepts, technical terms, algorithms, data structures,
  or named methods that a student MUST understand from this slide.
- Good examples: "bubble sort", "ArrayList", "dynamic resizing", "FIFO",
  "binarySearch", "hashmap", "null reference", "linked list"
- Do NOT include: generic words (value, type, size, element, method),
  instructional phrases (to construct, any list of, storing a value),
  professor names, university names, course codes, dates, or slide titles.
- If the slide has no educational content, return {"keyphrases": []}
- Return ONLY the JSON object. No explanation."""

_KP_FALLBACK_USER = """SLIDE TEXT:
{slide_text}"""

def _llm_extract_keyphrases(sc) -> List[Keyphrase]:
    """
    LLM fallback for slides where GLiNER returns 0 phrases.
    Self-contained: creates its own Groq client using the environment key
    so this step runs independently of Step 4.
    """
    # Build clean slide text — strip <br> and HTML artifacts
    all_text = " ".join(sc.headings + sc.body_text + sc.bullets + sc.captions)
    table_text = " ".join(
        re.sub(r"<br\s*/?>", " ", c).strip() for c in sc.table_cells
    )
    slide_text = re.sub(r"\s{2,}", " ",
                        (all_text + " " + table_text).strip())
    if not slide_text or len(slide_text.split()) < 10:
        return []

    groq_key = os.environ.get("GROQ_API_KEY", "")
    if not groq_key:
        print(f"    LLM fallback skipped ({sc.slide_id}): GROQ_API_KEY not set")
        return []

    try:
        from langchain_groq import ChatGroq
        from langchain_core.messages import HumanMessage, SystemMessage

        _kp_llm = ChatGroq(
            model="llama-3.3-70b-versatile",
            temperature=0,
            model_kwargs={"response_format": {"type": "json_object"}},
        )
        response = _kp_llm.invoke([
            SystemMessage(content=_KP_FALLBACK_SYSTEM),
            HumanMessage(content=_KP_FALLBACK_USER.format(slide_text=slide_text)),
        ])
        raw = json.loads(response.content)
        phrases = raw.get("keyphrases", [])
        if not isinstance(phrases, list):
            return []
    except Exception as e:
        print(f"    LLM keyphrase fallback error on {sc.slide_id}: {e}")
        return []

    kps: List[Keyphrase] = []
    seen: set = set()
    for phrase in phrases:
        phrase = str(phrase).lower().strip()
        if not phrase or phrase in seen:
            continue
        if not _is_valid_keyphrase(phrase):
            continue
        seen.add(phrase)
        src = _assign_source_type(phrase, sc)
        app = _find_sentence(phrase, sc.clean_text or slide_text)
        kps.append(Keyphrase(
            phrase=phrase,
            score=0.40,          # fixed score — below GLiNER confident, above noise
            source_type=src,
            slide_id=sc.slide_id,
            doc_id=sc.doc_id,
            appears_in=app,
        ))

    kps = _deduplicate(kps)
    kps.sort(key=lambda k: k.score, reverse=True)
    print(f"    LLM fallback: {len(kps)} phrases for {sc.slide_id}")
    return kps[:KEYPHRASE_MAX]

# -- Main extraction ----------------------------------------------------------
def extract_keyphrases(sc) -> List[Keyphrase]:
    if not sc.clean_text.strip():
        return []

    heading_text = " ".join(sc.headings)

    # For table-only slides: pair cells as "METHOD: description." sentences
    # so GLiNER sees them as definition phrases rather than a flat token dump.
    if sc.table_cells and not sc.bullets and not sc.body_text:
        cells = sc.table_cells
        table_text_parts = []
        for idx in range(0, len(cells) - 1, 2):
            k = re.sub(r"<br\s*/?>", " ", cells[idx]).strip()
            v = re.sub(r"<br\s*/?>", " ", cells[idx + 1]).strip()
            if k and v and not _is_noise(k) and not _is_noise(v):
                table_text_parts.append(f"{k}: {v}.")
        if len(cells) % 2 == 1:
            table_text_parts.append(re.sub(r"<br\s*/?>", " ", cells[-1]).strip())
        table_text = " ".join(table_text_parts)
    else:
        table_text = " ".join(
            re.sub(r"<br\s*/?>", " ", c) for c in sc.table_cells
        )

    rest_text = " ".join(sc.body_text + sc.bullets + sc.captions)
    if table_text:
        rest_text = (rest_text + " " + table_text).strip()

    extract_text = (heading_text + ". " + rest_text).strip() \
                   if heading_text and rest_text else (heading_text or rest_text)

    # Strip ALL HTML artifacts before any model sees the text.
    # This fixes the <br> garbage that was causing spaCy to produce broken
    # phrases like ") binarysearch(array" and "sorted tree structure quick sort".
    extract_text = re.sub(r"<[^>]+>", " ", extract_text)
    extract_text = re.sub(r"\s{2,}", " ", extract_text).strip()

    if not extract_text.strip():
        return []

    # ── GLiNER inference ──────────────────────────────────────────────────────
    try:
        entities = gliner.predict_entities(
            extract_text,
            ENTITY_LABELS,
            threshold=GLINER_THRESHOLD,
        )
    except Exception as e:
        print(f"    GLiNER error on {sc.slide_id}: {e}")
        entities = []

    # Collapse duplicate spans — keep highest score per phrase
    best: Dict[str, float] = {}
    for ent in entities:
        phrase = ent["text"].lower().strip()
        score  = float(ent["score"])
        if len(phrase) < 3:
            continue
        if not _is_valid_keyphrase(phrase):
            continue
        if phrase not in best or score > best[phrase]:
            best[phrase] = score

    # Build Keyphrase objects from GLiNER results
    kps: List[Keyphrase] = []
    for phrase, score in best.items():
        src   = _assign_source_type(phrase, sc)
        final = min(score + HEADING_BOOST, 1.0) if src == "heading" else score
        app   = _find_sentence(phrase, sc.clean_text)
        kps.append(Keyphrase(
            phrase=phrase,
            score=round(final, 4),
            source_type=src,
            slide_id=sc.slide_id,
            doc_id=sc.doc_id,
            appears_in=app,
        ))

    kps = _deduplicate(kps)
    kps.sort(key=lambda k: k.score, reverse=True)

    # ── LLM fallback (replaces old spaCy fallback) ────────────────────────────
    # Fires ONLY when GLiNER found nothing AND the slide has real content.
    # The LLM understands context — it won't extract "grocery store" from a
    # queue slide or produce broken tokens from <br>-polluted table text.
    if len(kps) <= 1 and len(extract_text.split()) >= 20:
        kps = _llm_extract_keyphrases(sc)

    return kps[:KEYPHRASE_MAX]

