# PDF_PATH, DOC_ID, STEM defined in the upload cell above.

# ── Data model ────────────────────────────────────────────────────────────────
@dataclass
class SlideMarkdown:
    slide_id:     str
    page_number:  int
    raw_markdown: str
    doc_id:       str

# ── Helper: empty-page check ─────────────────────────────────────────────────
def _is_empty_page(markdown: str) -> bool:
    """
    Return True when Marker found no real text on a page.

    FIX S1-2: strips {N} slide-number tags before the length check so that a
    page containing only e.g. '{3}' is correctly identified as empty.
    """
    cleaned = markdown.strip()
    if not cleaned:
        return True
    # Strip image markdown, basic formatting, and {N} slide-number tags
    text_only = re.sub(r"!\[.*?\]\(.*?\)", "", cleaned)
    text_only = re.sub(r"\{\d+\}",         "", text_only)   # FIX S1-2
    text_only = re.sub(r"[#>\-\*\|]",      "", text_only).strip()
    return len(text_only) < 10

# ── Helper: duplicate-page detection (FIX S1-1) ──────────────────────────────
def _word_set(markdown: str) -> set:
    """Lower-cased word tokens, stripped of punctuation."""
    return set(re.sub(r"[^a-z0-9]", " ", markdown.lower()).split())

def _is_duplicate_page(
    new_md: str,
    existing_slides: List[SlideMarkdown],
    window: int = 2,
    threshold: float = 0.50,
) -> bool:
    """
    Return True if new_md is substantially similar to one of the last `window`
    slides already added.

    Uses Jaccard similarity on word sets.  A threshold of 0.50 catches the
    OCR-title / Marker-title duplicate pattern (confirmed Jaccard = 0.520 on
    real data) while leaving genuinely different slides untouched.

    Guards:
    - Requires at least 5 distinct words in the new page (avoids false-positives
      on near-empty decorative slides).
    - Only compares against the most recent `window` slides, not the whole deck.
    """
    if not existing_slides:
        return False
    new_words = _word_set(new_md)
    if len(new_words) < 5:
        return False
    for prev in existing_slides[-window:]:
        prev_words = _word_set(prev.raw_markdown)
        if not prev_words:
            continue
        intersection = len(new_words & prev_words)
        union        = len(new_words | prev_words)
        if union > 0 and intersection / union >= threshold:
            return True
    return False

# ── Helper: OCR fallback ──────────────────────────────────────────────────────
def _ocr_page(doc: fitz.Document, page_number: int) -> str:
    """Render page at 3× zoom and run Tesseract (page_number is 1-indexed)."""
    page = doc[page_number - 1]
    mat  = fitz.Matrix(3, 3)
    pix  = page.get_pixmap(matrix=mat)
    img  = Image.frombytes("RGB", [pix.width, pix.height], pix.samples)
    return pytesseract.image_to_string(img, config="--psm 6").strip()

