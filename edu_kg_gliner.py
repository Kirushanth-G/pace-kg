# ── Structural label detector ─────────────────────────────────────────────────
def _is_structural_label(phrase: str) -> bool:
    """Return True if phrase is an instructional slide label, not a concept."""
    p = phrase.strip().lower()
    _starters = (
        "to ", "any ", "how ", "what ", "when ", "where ", "why ",
        "see ", "let ", "note ", "given ", "recall ", "consider ",
    )
    if any(p.startswith(s) for s in _starters) and len(p.split()) >= 4:
        return True
    # Gerund-led instructional phrases (e.g. "storing a value", "declaring and using")
    words = p.split()
    if words and words[0].endswith("ing") and len(words) >= 3:
        return True
    return False

# ── Build expanded keyphrase lookup set ──────────────────────────────────────
def _build_kp_set(kps) -> Set[str]:
    """
    Build the set of valid keyphrase strings for Layer 1 anchor validation.

    FIX: LLM-extracted keyphrases for method/API slides contain parentheses,
    e.g. 'add(value)', 'get(index)', 'indexOf(value)'.
    When the Step 4 LLM generates a triple it may use the bare name 'add' or
    'get' without parentheses, which would fail a strict set lookup.

    Solution: for every phrase that contains parentheses, also add the
    version with the parentheses and their contents stripped.
    e.g. 'add(value)' → also adds 'add'
         'get(index)' → also adds 'get'
         'binarySearch(array, value)' → also adds 'binarysearch'

    This is one-directional: we expand the lookup set, not the phrase itself,
    so the stored triple subject/object still maps back to the canonical phrase.
    """
    kp_set: Set[str] = set()
    for k in kps:
        p = k.phrase.lower()
        kp_set.add(p)
        # Strip parenthesised argument lists: add(value) → add
        stripped = re.sub(r"\s*\(.*?\)", "", p).strip()
        if stripped and stripped != p and len(stripped) >= 2:
            kp_set.add(stripped)
        # Also strip trailing () with no args: clear() → clear
        no_parens = p.replace("()", "").strip()
        if no_parens and no_parens != p and len(no_parens) >= 2:
            kp_set.add(no_parens)
    return kp_set

def _resolve_phrase(phrase: str, kps) -> str:
    """
    Given a phrase that may be a stripped version (e.g. 'add'), return
    the canonical keyphrase (e.g. 'add(value)') if one exists.
    Falls back to the phrase itself if no match found.
    """
    p = phrase.lower().strip()
    # Direct match first
    for k in kps:
        if k.phrase.lower() == p:
            return k.phrase.lower()
    # Try to find a keyphrase whose stripped form matches
    for k in kps:
        stripped = re.sub(r"\s*\(.*?\)", "", k.phrase.lower()).strip()
        no_parens = k.phrase.lower().replace("()", "").strip()
        if stripped == p or no_parens == p:
            return k.phrase.lower()
    return p

# ── 3-layer validator ─────────────────────────────────────────────────────────
def validate_triple(
    raw: dict,
    kp_set: Set[str],
    kps,
    slide_text: str,
    slide_id: str,
    doc_id: str,
    is_llm_slide: bool = False,
) -> Optional[Triple]:
    """
    Validate a raw LLM-generated triple through 3 layers.

    is_llm_slide: True when ALL keyphrases on this slide came from the LLM
    fallback (score == 0.40).  These slides have flat bullet-list clean_text
    rather than full prose sentences, so the evidence SBERT similarity is
    measured against a lower threshold (EVIDENCE_SIMILARITY_THRESHOLD_LLM)
    to avoid rejecting valid triples due to the terse text structure.
    """
    subject  = str(raw.get("subject",   "")).lower().strip()
    obj      = str(raw.get("object",    "")).lower().strip()
    relation = str(raw.get("relation",  "")).strip()
    evidence = str(raw.get("evidence",  "")).strip()
    try:
        conf = float(raw.get("confidence", 0))
    except (TypeError, ValueError):
        conf = 0.0

    # ── Layer 1: anchor + schema ──────────────────────────────────────────────
    if subject not in kp_set or obj not in kp_set:
        return None
    if subject == obj:
        return None
    if relation not in _VALID_RELATIONS:
        return None
    if _is_structural_label(subject) or _is_structural_label(obj):
        return None

    # Resolve to canonical phrase (handles stripped parentheses)
    subject = _resolve_phrase(subject, kps)
    obj     = _resolve_phrase(obj, kps)
    if subject == obj:
        return None

    # ── Layer 2: evidence verification ────────────────────────────────────────
    if not evidence:
        return None
    ev_emb   = sbert.encode(evidence,   convert_to_tensor=True, show_progress_bar=False)
    text_emb = sbert.encode(slide_text, convert_to_tensor=True, show_progress_bar=False)
    sim = float(st_util.cos_sim(ev_emb, text_emb))
    threshold = EVIDENCE_SIMILARITY_THRESHOLD_LLM if is_llm_slide else EVIDENCE_SIMILARITY_THRESHOLD
    if sim < threshold:
        return None

    # ── Layer 3: confidence threshold ─────────────────────────────────────────
    if conf < TRIPLE_CONFIDENCE_THRESHOLD:
        return None

    return Triple(
        subject=subject, relation=relation, object=obj,
        evidence=evidence, confidence=conf,
        slide_id=slide_id, doc_id=doc_id,
    )

# ── Semantic deduplication helpers ───────────────────────────────────────────
def _phrases_are_synonyms(a: str, b: str) -> bool:
    """
    Return True when two keyphrase strings refer to the same concept and
    should be treated as identical for deduplication purposes.

    Two cases handled — both are generic (no hardcoding):

    1. Acronym / expansion pairs:
       One string is an acronym (short all-alpha, ≤6 chars) and the other
       is its expansion — the acronym matches the first letter of EVERY word
       in the expansion (no stop-word filtering, to handle cases like
       "first in, first out" → f,i,f,o → "fifo").
       e.g. "fifo" vs "first in, first out"
            "lifo" vs "last in, first out"
            "api"  vs "application programming interface"

    2. Substring containment with word-boundary guard:
       One phrase is a clean substring of the other AND the match ends at a
       word boundary (space or end of string) to avoid false positives like
       "array" matching inside "arraylist".
       e.g. "ordered list" vs "ordered list of values" ✓
            "array" vs "arraylist"                     ✗ (no word boundary)
       Capped at 5 words for the shorter phrase.
    """
    a, b = a.lower().strip(), b.lower().strip()
    if a == b:
        return True

    # ── Case 1: acronym / expansion ───────────────────────────────────────────
    def _is_acronym_of(short: str, long: str) -> bool:
        """Return True if `short` is the acronym formed from initials of `long`."""
        short_clean = re.sub(r"[^a-z]", "", short)
        if len(short_clean) < 2 or len(short_clean) > 6:
            return False
        # Use ALL word initials (no stop-word filtering) so that
        # "first in, first out" → [f,i,f,o] → "fifo" matches correctly
        long_words = re.sub(r"[^a-z\s]", "", long).split()
        if not long_words:
            return False
        all_initials = "".join(w[0] for w in long_words if w and w[0].isalpha())
        return short_clean == all_initials

    if _is_acronym_of(a, b) or _is_acronym_of(b, a):
        return True

    # ── Case 2: substring containment with word-boundary guard ────────────────
    shorter, longer = (a, b) if len(a) <= len(b) else (b, a)
    shorter_words = shorter.split()
    if len(shorter_words) < 1 or len(shorter_words) > 5:
        return False
    idx = longer.find(shorter)
    if idx == -1:
        return False
    # Word-boundary check: char immediately after the match must be a space or EOL
    after = longer[idx + len(shorter):]
    if after and after[0] != " ":
        return False   # e.g. "array" inside "arraylist" — next char is "l", not " "
    # Also require they share the same first word
    if not longer.startswith(shorter_words[0]):
        return False
    return True


def _dedup_triples(triples: List[Triple]) -> List[Triple]:
    """
    Deduplicate triples within the same slide.

    Two deduplication passes:

    Pass 1 — Exact pair dedup:
      For any two triples with the same (subject, object) pair, keep only
      the one with the highest confidence. Handles cases where the LLM
      generates both isPartOf and isExampleOf for the same pair.

    Pass 2 — Semantic pair dedup:
      For any two triples where the subjects are synonyms AND the objects
      are synonyms (or vice versa), keep only the higher-confidence one.
      This handles acronym/expansion duplicates like:
        "queue isDefinedAs fifo" + "queue isDefinedAs first in, first out"
      where the two objects mean the same thing but are different strings,
      so Pass 1 would not catch them.

    Both passes are fully generic — no domain-specific knowledge used.
    """
    if not triples:
        return []

    # Pass 1: exact (subject, object) dedup
    best: Dict[Tuple[str, str], Triple] = {}
    for t in triples:
        key = (t.subject, t.object)
        if key not in best or t.confidence > best[key].confidence:
            best[key] = t
    triples = list(best.values())

    # Pass 2: semantic dedup — O(n²) but n is small per slide (≤ 10 triples)
    kept: List[Triple] = []
    for t in sorted(triples, key=lambda x: -x.confidence):
        is_semantic_dup = False
        for k in kept:
            # Same relation, semantically equivalent subject AND object
            subj_match = _phrases_are_synonyms(t.subject, k.subject)
            obj_match  = _phrases_are_synonyms(t.object,  k.object)
            # OR reversed direction (both encode same real-world relationship)
            subj_obj_match = _phrases_are_synonyms(t.subject, k.object)
            obj_subj_match = _phrases_are_synonyms(t.object,  k.subject)
            if (subj_match and obj_match) or (subj_obj_match and obj_subj_match):
                is_semantic_dup = True
                break
        if not is_semantic_dup:
            kept.append(t)

    return kept

