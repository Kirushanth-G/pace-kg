def _is_slide_label_phrase(phrase: str) -> bool:
    p = phrase.strip().lower()
    if any(p.startswith(s) for s in _LABEL_STARTERS) and len(p.split()) >= 4:
        return True
    return False

def _is_valid_keyphrase(phrase: str) -> bool:
    """Return True when a phrase is a genuine educational concept.
    Rules are fully generic — no domain hardcoding.
    """
    p = phrase.strip().lower()
    if len(p) < 3:
        return False
    if p in _KP_GENERIC_WORDS:
        return False
    if _is_slide_label_phrase(p):
        return False
    words = p.split()
    if words and words[0] in ("this", "that", "these", "those", "its", "their"):
        return False
    if re.fullmatch(r"[\d\s\.\,\-]+", p):
        return False
    return True

