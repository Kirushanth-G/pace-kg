"""## Step 2 — Markdown Preprocessor

Four stages applied to the raw markdown from Step 1:

1. **Structural parsing** — lines routed into typed buckets: headings, bullets, table cells, captions, body text
2. **Noise removal** — page numbers, copyright lines, URLs, image tags discarded
3. **Cross-slide repetition filter** — blocks appearing on >50% of slides removed (recurring headers/footers)
4. **Assembly** — buckets joined into `clean_text`; `heading_phrases` stored for Step 3 boost

OCR-recovered lines (prefixed `> OCR:`) are re-classified into proper buckets instead of being dumped into captions.

**Output:** `SlideContent` per slide.
"""

import re
from collections import Counter
from dataclasses import asdict, dataclass, field
from pathlib import Path
from typing import List

# ── Data model ────────────────────────────────────────────────────────────────
@dataclass
class SlideContent:
    slide_id:        str
    page_number:     int
    doc_id:          str
    headings:        List[str] = field(default_factory=list)
    body_text:       List[str] = field(default_factory=list)
    bullets:         List[str] = field(default_factory=list)
    table_cells:     List[str] = field(default_factory=list)
    captions:        List[str] = field(default_factory=list)
    code_lines:      List[str] = field(default_factory=list)
    heading_phrases: List[str] = field(default_factory=list)
    clean_text:      str       = ""
    ocr_applied:     bool      = False

# ── Noise patterns ────────────────────────────────────────────────────────────
_NOISE_PATTERNS = [
    re.compile(r"^\s*page\s+\d+\s*(of\s+\d+)?\s*$",  re.IGNORECASE),
    re.compile(r"^\s*\d+\s*$"),
    # FIX S2-1: was r"^[cC].*" — matched ANY text starting with C/c.
    # Now targets only genuine copyright notices.
    re.compile(r"^(?:©|\(c\)|copyright\b)",           re.IGNORECASE),
    re.compile(r"^\s*(references|bibliography)\s*$",   re.IGNORECASE),
    re.compile(r"https?://\S+"),
    re.compile(r"^\s*\[\d+\]"),
    re.compile(r"^!\[.*?\]\(.*?\)$"),
    re.compile(r"^quiz\s*:",                           re.IGNORECASE),
    re.compile(r"^\{\d+\}$"),                          # {N} slide-number tags
    re.compile(
        r"^(input|output|legend|infrastructure)"
        r"(\s+replaced.*|\s+kept.*|\s+storage.*)?$",
        re.IGNORECASE,
    ),
]

def _is_noise(text: str) -> bool:
    t = text.strip()
    return bool(t) and any(p.match(t) for p in _NOISE_PATTERNS)

# ── Code-line detector ────────────────────────────────────────────────────────
# FIX S2-3: removed '<' and '>' from code_chars — they fire on inequality
# lists and prose type-parameter examples, producing false positives.
_CODE_CHARS = frozenset("{}();=[]")

def _is_code_line(text: str) -> bool:
    """
    Heuristic classifier: return True when a text fragment looks like source
    code rather than natural-language prose.

    Changes vs original
    -------------------
    S2-3  '<' and '>' removed from _CODE_CHARS (see module docstring).
    S2-2  func-call rule threshold raised to n >= 3 AND capped at
          word_count <= 8 so long prose sentences are not caught.
    S2-4  camelCase rule now requires fewer than 2 commas so that token
          enumerations like '<, >, compareTo, …' are not misclassified.
    S2-6  New dot-access rule for short identifier.property expressions.
    """
    t = text.strip()
    if not t:
        return False
    if t.startswith("```"):
        return True

    n          = sum(1 for c in t if c in _CODE_CHARS)
    word_count = len(t.split())

    # Primary signal: high density of code punctuation
    if len(t) > 5 and n / len(t) > 0.08:
        return True

    # camelCase identifier  (FIX S2-4: exclude comma-separated token lists)
    if re.search(r"\b[a-z]+[A-Z][a-zA-Z]+\b", t):
        if word_count <= 5 and t.count(",") < 2:
            return True

    # snake_case identifier
    if re.search(r"\b[a-z]+_[a-z]+\b", t):
        return True

    # Comparison / assignment operators in spacing context
    if re.search(r"\s(==|!=|<=|>=|&&|\|\||:=|->|=>)\s", t):
        return True

    # Line terminators typical of C-family code
    if t.endswith((";", "{", "}")):
        return True

    # Comment prefixes
    if re.match(r"^\s*(//|#\s|--|/\*|\*\s)", t):
        return True

    # Method / function call  (FIX S2-2: n >= 3 AND word_count <= 8)
    if re.search(r"\b\w{2,}\(", t) and n >= 3 and word_count <= 8:
        return True

    # Constructor declaration
    if re.match(r"^(default\s+)?constructor\s+\w+\s*\(", t, re.IGNORECASE):
        return True

    # Python dunder method
    if re.match(r"^__\w+__\s*\(", t):
        return True

    # FIX S2-6: dot-notation access on very short lines  (e.g. scores.length)
    if re.search(r"\b[a-z]\w*\.[a-z]\w*\b", t) and word_count <= 3:
        return True

    return False

# ── Inline markdown stripper ──────────────────────────────────────────────────
_INLINE_MD = re.compile(
    r"(\*\*|__)(.*?)\1|(\*|_)(.*?)\3|`([^`]+)`|~~(.*?)~~|\[([^\]]+)\]\([^)]+\)",
    re.DOTALL,
)

def _strip_inline(text: str) -> str:
    prev = None
    while prev != text:
        prev = text
        text = _INLINE_MD.sub(
            lambda m: next(
                g for g in m.groups()
                if g is not None and g not in ("**", "__", "*", "_")
            ),
            text,
        )
    return text.strip()

# ── Bullet-marker regex ───────────────────────────────────────────────────────
# Covers ASCII bullets (- * +) and Unicode geometric shapes / dingbats used
# by Beamer and other academic PDF tools as slide bullet markers.
_BULLET_RE = re.compile(
    r"^\s*[-*+]\s*(?:[\u2022\u25A0-\u25FF\u2700-\u27BF]\s*)?|"
    r"^\s*[\u2022\u25A0-\u25FF\u2700-\u27BF]\s+"
)
_BEAMER_BULLET_RE = re.compile(r"^[\u2022\u25A0-\u25FF\u2700-\u27BF]\s*")

# ── OCR noise detector ────────────────────────────────────────────────────────
def _is_ocr_noise(text: str) -> bool:
    t = text.strip()
    if not t or len(t) < 4:
        return True
    words = t.split()
    if len(words) == 1 and not words[0].isalpha():
        return True
    non_ascii = sum(1 for c in t if ord(c) > 127)
    if non_ascii / len(t) > 0.1:
        return True
    letters = sum(1 for c in t if c.isalpha())
    if len(t) > 5 and letters / len(t) < 0.4:
        return True
    return False

# ── FIX S2-5: prose-label detector for mixed code/prose fences ───────────────
def _is_prose_label(line: str) -> bool:
    """
    Return True when a line inside a ``` code fence is actually a prose bullet
    label (e.g. '• construction', '• storing a value') that should be routed
    to bullets rather than code_lines.

    Criteria:
    - Starts with a bullet marker (ASCII or Unicode) or '•'
    - After stripping the marker, contains at least one word
    - Contains zero _CODE_CHARS (no assignment, parens, brackets, etc.)

    This is intentionally strict to avoid accidentally routing real code lines
    (which will always contain at least one code char) to the wrong bucket.
    """
    s = line.strip()
    is_bullet = bool(_BULLET_RE.match(s)) or s.startswith("•")
    if not is_bullet:
        return False
    inner = _BULLET_RE.sub("", s).lstrip("•").strip()
    if not inner:
        return False
    return sum(1 for c in inner if c in _CODE_CHARS) == 0

# ── OCR line re-classifier ────────────────────────────────────────────────────
def _classify_ocr_line(text: str, sc: SlideContent) -> None:
    """Re-classify a single OCR-recovered line into the appropriate bucket."""
    stripped = text.strip()
    if not stripped or _is_noise(stripped) or _is_ocr_noise(stripped):
        return
    if _is_code_line(stripped):
        sc.code_lines.append(stripped)
        return
    if stripped.startswith("#"):
        inner = re.sub(r"^#+\s*", "", stripped).strip()
        if inner:
            sc.headings.append(inner)
            sc.heading_phrases.append(inner)
    elif re.match(r"^[-*+¢—>]\s+", stripped):
        inner = _BULLET_RE.sub("", stripped).strip()
        if inner:
            sc.bullets.append(inner)
    elif stripped.startswith("|") and stripped.endswith("|"):
        if not re.match(r"^\|[\s\-\|]+\|$", stripped):
            cells = [c.strip() for c in stripped.split("|") if c.strip()]
            sc.table_cells.extend(cells)
    else:
        sc.body_text.append(stripped)

# ── Stage 1+2: parse a single slide ──────────────────────────────────────────
def _parse_slide(slide_md: SlideMarkdown) -> SlideContent:
    sc = SlideContent(
        slide_id    = slide_md.slide_id,
        page_number = slide_md.page_number,
        doc_id      = slide_md.doc_id,
    )

    lines       = slide_md.raw_markdown.splitlines()
    in_fence    = False   # are we currently inside a ``` code fence?

    for line in lines:
        s = line.strip()
        if not s:
            continue

        # ── Code fence toggle ─────────────────────────────────────────────────
        if s.startswith("```"):
            in_fence = not in_fence
            continue

        # ── Inside a code fence ───────────────────────────────────────────────
        if in_fence:
            # FIX S2-5: route prose bullet labels to bullets, not code_lines
            if _is_prose_label(line):
                inner = _BULLET_RE.sub("", s).lstrip("•").strip()
                inner = _strip_inline(inner)
                if inner and not _is_noise(inner):
                    sc.bullets.append(inner)
            else:
                if s and not _is_noise(s):
                    sc.code_lines.append(s)
            continue

        # ── Headings ──────────────────────────────────────────────────────────
        if s.startswith("#"):
            text = _strip_inline(re.sub(r"^#+\s*", "", s))
            if text and not _is_noise(text):
                sc.headings.append(text)
                sc.heading_phrases.append(text)

        # ── Blockquotes: captions or OCR-recovered lines ──────────────────────
        # FIX S2-OCR: this branch MUST appear before the bullet regex because
        # '>' is included in the bullet character class r'^[-*¢—>]\s+'.
        # If the bullet branch fired first, '> OCR: ...' lines would be routed
        # to bullets with the "OCR: " prefix intact instead of being properly
        # re-classified by _classify_ocr_line().
        elif s.startswith(">"):
            inner = re.sub(r"^>\s*", "", s).strip()
            if inner.startswith("OCR:"):
                sc.ocr_applied = True
                _classify_ocr_line(inner[4:].strip(), sc)
            else:
                text = _strip_inline(inner)
                if text and not _is_noise(text):
                    sc.captions.append(text)

        # ── Standard markdown bullets (- * ¢ —) ──────────────────────────────
        # Note: '>' intentionally removed from this pattern — handled above.
        elif re.match(r"^[-*¢—]\s+", s) and not s.startswith(("**", "*a", "*b", "*c")):
            m    = re.match(r"^[-*¢—]\s+(.+)", s)
            text = _strip_inline(m.group(1)) if m else ""
            if text and not _is_noise(text):
                if _is_code_line(text):
                    sc.code_lines.append(text)
                else:
                    sc.bullets.append(text)

        # ── Beamer / Unicode geometric shape bullets ──────────────────────────
        elif _BEAMER_BULLET_RE.match(s):
            text = _strip_inline(_BULLET_RE.sub("", s)).strip()
            text = _BULLET_RE.sub("", text).strip()
            if text and not _is_noise(text):
                if _is_code_line(text):
                    sc.code_lines.append(text)
                else:
                    sc.bullets.append(text)

        # ── Table rows ────────────────────────────────────────────────────────
        elif s.startswith("|") and s.endswith("|"):
            cells = [_strip_inline(c.strip()) for c in s.split("|") if c.strip()]
            if all(re.fullmatch(r"[-:]+", c) for c in cells):
                continue   # markdown separator row
            # FIX S2-TABLE-HEADER: Skip pure header rows — rows where every
            # cell is very short (≤ 2 words) and looks like a column label
            # (e.g. "Operation | Description", "Method name | Description").
            # These create noise keyphrases like "operation" and cause wrong
            # triples like "dequeue isPartOf operation".
            # A header row has: all cells ≤ 2 words, all cells title-case or
            # all-caps, and no cell contains digits or parentheses.
            def _looks_like_header_cell(cell: str) -> bool:
                words = cell.split()
                if len(words) > 3:
                    return False
                if any(c.isdigit() or c in "()" for c in cell):
                    return False
                return True
            if cells and all(_looks_like_header_cell(c) for c in cells):
                continue   # skip header row
            sc.table_cells.extend(c for c in cells if c and not _is_noise(c))

        # ── Body text (everything else) ───────────────────────────────────────
        else:
            text = _strip_inline(s)
            if not text or _is_noise(text):
                continue
            if _is_code_line(text):
                sc.code_lines.append(text)
            else:
                sc.body_text.append(text)

    return sc

# ── Stage 3: cross-slide repetition filter ───────────────────────────────────
def _cross_slide_filter(contents: List[SlideContent]) -> List[SlideContent]:
    total = len(contents)
    if total < 5:
        print(f"Cross-slide filter skipped (only {total} slides — need ≥ 5)")
        return contents
    threshold = total * 0.5
    counts: Counter = Counter()
    for sc in contents:
        seen: set = set()
        for bucket in (sc.headings, sc.body_text, sc.bullets, sc.table_cells, sc.captions):
            for t in bucket:
                if t not in seen:
                    counts[t] += 1
                    seen.add(t)
    noise = {t for t, c in counts.items() if c > threshold}
    if noise:
        print(f"Cross-slide filter removed {len(noise)} repeated block(s):")
        for n in sorted(noise):
            print(f"  '{n[:80]}'")
    else:
        print("Cross-slide filter: nothing removed")
    for sc in contents:
        sc.headings    = [t for t in sc.headings    if t not in noise]
        sc.body_text   = [t for t in sc.body_text   if t not in noise]
        sc.bullets     = [t for t in sc.bullets     if t not in noise]
        sc.table_cells = [t for t in sc.table_cells if t not in noise]
        sc.captions    = [t for t in sc.captions    if t not in noise]
    return contents

# ── Stage 4: assemble clean_text ─────────────────────────────────────────────
def _assemble(sc: SlideContent) -> None:
    parts        = sc.headings + sc.body_text + sc.bullets + sc.table_cells + sc.captions
    sc.clean_text = " ".join(parts).strip()

# ── Run all stages ────────────────────────────────────────────────────────────
